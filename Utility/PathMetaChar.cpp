#include <Utility/PathMetaChar.hpp>

#include <cstring>

// ////////////////////////////////////////////////////////////////////////////

namespace MLB {

namespace Utility {

// ////////////////////////////////////////////////////////////////////////////
#ifdef __MSDOS__
const char PathNameMetaChar_String[] = "\\/:";
#elif _Windows
const char PathNameMetaChar_String[] = "\\/:";
#elif _VMS_
const char PathNameMetaChar_String[] = "[]:";
#else
const char PathNameMetaChar_String[] = "/";
#endif // #ifdef __MSDOS__
// ////////////////////////////////////////////////////////////////////////////

// ////////////////////////////////////////////////////////////////////////////
bool IsPathMetaChar(char in_path_char)
{
	return(in_path_char && ::strchr(PathNameMetaChar_String, in_path_char));
}
// ////////////////////////////////////////////////////////////////////////////

// ////////////////////////////////////////////////////////////////////////////
bool IsPathMetaChar(const char *in_path_string)
{
	return(IsPathMetaChar(*in_path_string));
}
// ////////////////////////////////////////////////////////////////////////////

// ////////////////////////////////////////////////////////////////////////////
const char *GetPathMetaChars()
{
	return(PathNameMetaChar_String);
}
// ////////////////////////////////////////////////////////////////////////////

} // namespace Utility

} // namespace MLB

