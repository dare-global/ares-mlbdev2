// ////////////////////////////////////////////////////////////////////////////
void ParseCfgItemDatum(const std::string &src, bool &dst)
{
	if ((src == "1")  								  ||
		 (!StringCompareIgnoreCase(src, "true")) ||
		 (!StringCompareIgnoreCase(src, "on"))   ||
		 (!StringCompareIgnoreCase(src, "yes")))
		dst = true;
	else if ((src == "0")							  ||
		(!StringCompareIgnoreCase(src, "false")) ||
		(!StringCompareIgnoreCase(src, "off"))   ||
		(!StringCompareIgnoreCase(src, "no")))
		dst = false;
	else
		throw std::invalid_argument("Unable to parse value '" +
			src + "' as a boolean.");
}
