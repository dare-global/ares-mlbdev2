// ////////////////////////////////////////////////////////////////////////////
std::size_t GetDirNameLength(const char *in_string)
{
	const char  *file_portion = GetFileNamePortion(in_string);
	std::size_t  dir_length   =
		static_cast<std::size_t >(file_portion - in_string);

	if (dir_length < 2)
		return(dir_length);

	return(((in_string[dir_length - 1] == '/') ||
		(in_string[dir_length - 1] == '\\')) ? (dir_length - 1) : dir_length);
}
// ////////////////////////////////////////////////////////////////////////////

// ////////////////////////////////////////////////////////////////////////////
std::size_t GetDirNameLength(const std::string &in_string)
{
	return(GetDirNameLength(in_string.c_str()));
}
// ////////////////////////////////////////////////////////////////////////////

// ////////////////////////////////////////////////////////////////////////////
std::string GetDirNamePortion(const std::string &in_string)
{
	std::size_t dir_length = GetDirNameLength(in_string);
	
	return((dir_length) ? std::string(in_string, 0, dir_length) : "");
}
